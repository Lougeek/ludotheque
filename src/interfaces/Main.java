    private void jTableAdherentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAdherentMouseClicked
        
        if (jTableAdherent.getSelectedRow() >= 0){
            switch (evt.getClickCount()){
                case 1:
                    jLabelLesJeuxEmpPar.setText(jTableAdherent.getValueAt(jTableAdherent.getSelectedRow(),6).toString());
                    break;
                case 2:
                    //Adherent.getInstance(2,jTableAdherent.getValueAt(jTableAdherent.getSelectedRow(),1).toString());
                    Adherent.getInstance().setFenetre(2, jTableAdherent.getValueAt(jTableAdherent.getSelectedRow(),1).toString());
                    break;
            }
        }   
    }//GEN-LAST:event_jTableAdherentMouseClicked

