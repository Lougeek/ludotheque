    private void jRadioButtonJeuxSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonJeuxSelectActionPerformed
        // TODO add your handling code here:

        jLabelLesJeuxSelect.setText(""+jTableJeux.getSelectedRowCount());
        if (jRadioButtonJeuxSelect.isSelected()){
             ArrayList jeux = new ArrayList<>();
            for(int i = 0; i < jTableJeux.getRowCount(); i++) {
                if (jTableJeux.isRowSelected(i)){
                    Jeu j = ((ModelTableJeux)jTableJeux.getModel()).getListeJeu().get(i);
                    jeux.add(j);
                }
            }
            jTableJeux.removeAll();
            ModelTableJeux temp = new ModelTableJeux();
            temp.setListeJeu(jeux);
            jTableJeux.setModel(temp);
        
        }
    }//GEN-LAST:event_jRadioButtonJeuxSelectActionPerformed

