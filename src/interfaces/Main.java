    private void jButtonSortieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSortieActionPerformed
        // TODO add your handling code here:     
        if (jTableJeux.getSelectedRow() >=0 ){
            ArrayList listJeuxSelect = new ArrayList();
            int rows = jTableJeux.getRowCount();
            for (int i = 0; i < rows ; i++){
                if (jTableJeux.isRowSelected(i)){                          
                    listJeuxSelect.add(""+jTableJeux.getValueAt(i,1).toString() + "  "+jTableJeux.getValueAt(i,3).toString());
                }
            }
            SortieChoix.getInstance(this,listJeuxSelect,jLabelLesJeuxEmpPar.getText().toString());
        }else{
            System.out.println("selection non");
            JeuChoix.getInstance(this,1,null,null);
        }
    }//GEN-LAST:event_jButtonSortieActionPerformed

